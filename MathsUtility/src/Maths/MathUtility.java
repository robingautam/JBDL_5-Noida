package Maths;

public class MathUtility {

    public static int add(int num1, int num2){
        return num1+num2;
    }

    public static int max(int num1, int num2){
        return num1>num2?num1:num2;
    }

    public static int min(int num1, int num2){
        return num1>num2?num2:num1;
    }

}
